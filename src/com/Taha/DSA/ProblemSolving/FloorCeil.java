package com.Taha.DSA.ProblemSolving;

public class FloorCeil {

    public static void main(String... args){
        System.out.println(Math.floor(21.453));     // it gives the immediate before or equal number and ignores the decimal number  Ex for  23.42344 it will give 23 and for 54 it will give 54
        System.out.println(Math.ceil(21.453));      // Opposite of Floor
    }

}
